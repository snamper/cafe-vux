<template>
  <div class="cartcontrol">
    <transition name="move">
      <div class="cart-decrease" v-show="food.count>0" @click.stop.prevent="decreaseCart">
        <span class="inner iconfont icon-sub1"></span>
      </div>
    </transition>
    <div class="cart-count" v-show="food.count>0">{{food.count}}</div>
    <div class="cart-add iconfont icon-c_add" @click.stop.prevent="addCart"></div>
  </div>
</template>

<script type="text/ecmascript-6">
  // import Vue from 'vue';

  export default {
    props: {
      food: {
        type: Object
      }
    },
    methods: {
      addCart(event) {
        this.$store.commit('add_count_categorys', this.food);
        this.$emit('add', event.target);
      },
      decreaseCart() {
       this.$store.commit('sub_count_categorys', this.food);
      }
    }
  };
</script>

